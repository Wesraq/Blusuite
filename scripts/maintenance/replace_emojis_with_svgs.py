def replace_emojis_in_file(file_path):
    """Replace emojis with SVG icons in a single file"""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        original_content = content
        replacements_made = 0
        
        for emoji, svg in ICON_MAP.items():
            if emoji in content:
                # Count occurrences
                count = content.count(emoji)
                # Replace emoji with SVG
                content = content.replace(emoji, svg)
                replacements_made += count
        
        if replacements_made > 0:
            with open(file_path, 'w', encoding='utf-8') as f:
                f.write(content)
            print(f"✓ {file_path.name}: {replacements_made} replacements")
            return replacements_made
        
        return 0
    
    except Exception as e:
        print(f"✗ Error processing {file_path}: {e}")
        return 0


def main():
    """Main function to process all templates"""
    templates_dir = Path(__file__).parent / 'templates' / 'ems'
    
    if not templates_dir.exists():
        print(f"Error: Templates directory not found at {templates_dir}")
        return
    
    print("=" * 60)
    print("EMS Icon Replacement Script")
    print("=" * 60)
    print(f"Scanning: {templates_dir}")
    print()
    
    total_files = 0
    total_replacements = 0
    
    # Process all HTML files
    for html_file in sorted(templates_dir.glob('*.html')):
        replacements = replace_emojis_in_file(html_file)
        if replacements > 0:
            total_files += 1
            total_replacements += replacements
    
    print()
    print("=" * 60)
    print(f"Summary:")
    print(f"  Files modified: {total_files}")
    print(f"  Total replacements: {total_replacements}")
    print("=" * 60)


if __name__ == '__main__':
    main()
